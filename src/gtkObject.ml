open Gobject

let cast w : [`giu] obj = try_cast w "GInitiallyUnowned"
external _ref_sink : [>`giu] obj -> unit
    = "ml_g_object_ref_sink"
let make ~classe params =
  let obj = unsafe_create ~classe params in _ref_sink obj;
  obj
